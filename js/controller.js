import * as model from "./model.js";
import searchView from "./views/searchView.js";
import infoView from "./views/infoView.js";
import mapView from "./views/mapView.js";

const controlSearch = async (event) => {
    event.preventDefault();
    searchView.clearError();

    const searchQuery = searchView.getQuery();

    const validation = validateSearchQuery(searchQuery);
    if (validation.isEmpty)
        return searchView.renderError(
            "Please provide an IP address or domain."
        );
    else if (!validation.isDomain && !validation.isIp)
        return searchView.renderError(
            "Please provide a valid IPv4, IPv6 address or domain."
        );

    searchAndRender(searchQuery, validation.isDomain);
};

const searchAndRender = async (searchQuery, isDomain = false) => {
    try {
        await model.search(searchQuery, isDomain);
        if (!model.state.ipInfo)
            throw new Error("Sorry, this search has no results.");
    } catch (error) {
        searchView.renderError(error);
    }
    infoView.renderSearch(model.state.ipInfo);
    const { lat, lng } = model.state.ipInfo.location;
    mapView.updateMarker(lat, lng);
};

const validateSearchQuery = (searchQuery) => {
    const isEmpty = !!!searchQuery;
    const isIp = /((^\s*((([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5])\.){3}([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5]))\s*$)|(^\s*((([0-9A-Fa-f]{1,4}:){7}([0-9A-Fa-f]{1,4}|:))|(([0-9A-Fa-f]{1,4}:){6}(:[0-9A-Fa-f]{1,4}|((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3})|:))|(([0-9A-Fa-f]{1,4}:){5}(((:[0-9A-Fa-f]{1,4}){1,2})|:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3})|:))|(([0-9A-Fa-f]{1,4}:){4}(((:[0-9A-Fa-f]{1,4}){1,3})|((:[0-9A-Fa-f]{1,4})?:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9A-Fa-f]{1,4}:){3}(((:[0-9A-Fa-f]{1,4}){1,4})|((:[0-9A-Fa-f]{1,4}){0,2}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9A-Fa-f]{1,4}:){2}(((:[0-9A-Fa-f]{1,4}){1,5})|((:[0-9A-Fa-f]{1,4}){0,3}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9A-Fa-f]{1,4}:){1}(((:[0-9A-Fa-f]{1,4}){1,6})|((:[0-9A-Fa-f]{1,4}){0,4}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(:(((:[0-9A-Fa-f]{1,4}){1,7})|((:[0-9A-Fa-f]{1,4}){0,5}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:)))(%.+)?\s*$))/.test(
        searchQuery
    );
    const isDomain = /^(?:[a-z0-9](?:[a-z0-9-]{0,61}[a-z0-9])?\.)+[a-z0-9][a-z0-9-]{0,61}[a-z0-9]$/.test(
        searchQuery
    );

    return {
        isEmpty,
        isIp,
        isDomain,
    };
};

const init = () => {
    searchView.addRenderHandler(controlSearch);
    // Render my IP and location
    searchAndRender("");
};

init();
